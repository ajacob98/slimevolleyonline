package com.alntech.slimevolleyonline.controller.pojo;

public class InitialData {
    int ballRadius;
    int floorWidth;
    int slimeRadius;

    public InitialData() {
        super();
    }

    public int getBallRadius() {
        return ballRadius;
    }

    public void setBallRadius(int ballRadius) {
        this.ballRadius = ballRadius;
    }

    public int getFloorWidth() {
        return floorWidth;
    }

    public void setFloorWidth(int floorWidth) {
        this.floorWidth = floorWidth;
    }

    public int getSlimeRadius() {
        return slimeRadius;
    }

    public void setSlimeRadius(int slimeRadius) {
        this.slimeRadius = slimeRadius;
    }
}
